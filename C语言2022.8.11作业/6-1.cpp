/*----------------------------------------------------------------------
【程序填空】
------------------------------------------------------------------------
题目：借助于数组b，对数组a中的0~100之间的整数，按升序输出。 

测试的N个整数
2   3   4   5  108  1  23   2   2   4  23  -5
处理后的数据是：
1   2   2   2   3   4   4   5  23  23  
------------------------------------------------------------------------
注意：除要求填空的位置之外，请勿改动程序中的其他部分。
----------------------------------------------------------------------*/
#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <ctype.h>
#include <string.h> 
   
#define N 12 

int main()
{
    int i,j,n=0,a[N]={2,3,4,5,108,1,23,2,2,4,23,-5},b[101]={0};
    for(i=0;i<N;i++)
        printf("%-5d",a[i]);
    printf("\n");
    for(i=0;i<N;i++)
    {
        if(a[i]>=0 && a[i]<=100)
/**************************SPACE******************************/
            b[a[i]]++;       
    }
    printf("处理后的数据是：\n");
    for(i=0;i<=100;i++)
    {        
        while(b[i]>0)
        {
            printf("%-5d",i);
/**************************SPACE******************************/
            b[i]--; 
        }        
    }    
    return 0;
}
